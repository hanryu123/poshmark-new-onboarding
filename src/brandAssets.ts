/** Pick `count` unique brand names, preferring `pool`, then `fallback`. */
export function pickUniqueBrands(
  count: number,
  pool: string[],
  exclude: Set<string>,
  fallback: string[]
): string[] {
  const out: string[] = [];
  const used = new Set(exclude);

  const tryPush = (name: string) => {
    if (used.has(name) || out.includes(name)) return false;
    used.add(name);
    out.push(name);
    return true;
  };

  const combined = [...pool, ...fallback];
  for (const name of combined) {
    if (out.length >= count) break;
    tryPush(name);
  }

  let i = 0;
  while (out.length < count && i < fallback.length * 3) {
    const name = fallback[i % fallback.length];
    i += 1;
    tryPush(name);
  }

  return out.slice(0, count);
}
